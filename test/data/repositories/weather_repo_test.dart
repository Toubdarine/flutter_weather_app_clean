              }
            ]
          }
        ]
      }
    };
    test('should return WeatherDataEntity when the call to api is successful',
        () async {
      // Arrange

      final weatherDataModel = WeatherDataModel.fromJson(tResponse);

      final expectedWeatherDataEntity = weatherDataModel.toWeatherDataEntity;

      when(mockApiClient.getReq(Uri.parse(apiUrl)))
          .thenAnswer((_) async => Right(http.Response(
                jsonEncode(tResponse),
                200,
              )));

      // Act
      final result = await repository.getWeatherForecaste(
          tLatitude, tLongitude, tDate, apiKey);

      // Assert
      expect(
          result, Right<Failure, WeatherDataEntity>(expectedWeatherDataEntity));
      verify(mockApiClient.getReq(Uri.parse(apiUrl))).called(1);
    });

    test('should return failure when the call to api fails', () async {
      final uri = Uri.parse(apiUrl);
      when(mockApiClient.getReq(uri))
          .thenAnswer((_) async => Left(ServerFailure('server error')));

      final result = await repository.getWeatherForecaste(
          tLatitude, tLongitude, tDate, apiKey);

      expect(result.isLeft(), true);
      expect(result,
          Left<Failure, WeatherDataEntity>(ServerFailure('Server Failure')));
    });
  });
